package project.data;

public class StatData {
    private int atk, def, spAtk, spDef, spd, acc, eva;

    public void setAtk(int atk) {
        this.atk = atk;
    }

    public void setDef(int def) {
        this.def = def;
    }

    public void setSpAtk(int spAtk) {
        this.spAtk = spAtk;
    }

    public void setSpDef(int spDef) {
        this.spDef = spDef;
    }

    public void setSpd(int spd) {
        this.spd = spd;
    }

    public void setAcc(int acc) {
        this.acc = acc;
    }
    
    public void setEva(int eva) {
        this.eva = eva;
    }

    public int getAtk() {
        return this.atk;
    }

    public int getDef() {
        return this.def;
    }

    public int getSpAtk() {
        return this.spAtk;
    }

    public int getSpDef() {
        return this.spDef;
    }

    public int getSpd() {
        return this.spd;
    }

    public int getAcc() {
        return this.acc;
    }
    
    public int getEva() {
        return this.eva;
    }
}
